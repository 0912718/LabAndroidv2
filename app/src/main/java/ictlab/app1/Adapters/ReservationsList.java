package ictlab.app1.Adapters;

public class ReservationsList {
    private String id, classroom_id, date, title, description, from_block, to_block, classroom_name;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClassroom_id() {
        return classroom_id;
    }

    public void setClassroom_id(String classroom_id) {
        this.classroom_id = classroom_id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getFrom_block() {
        return from_block;
    }

    public void setFrom_block(String from_block) {
        this.from_block = from_block;
    }

    public String getTo_block() {
        return to_block;
    }

    public void setTo_block(String to_block) {
        this.to_block = to_block;
    }

    public String getClassroom_name() {
        return classroom_name;
    }

    public void setClassroom_name(String classroom_name) {
        this.classroom_name = classroom_name;
    }
}
